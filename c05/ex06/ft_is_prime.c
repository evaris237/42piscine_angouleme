int	ft_is_prime(int nb)
{
	unsigned int	i;

	if ((nb < 3) || !(nb % 2))
		return (nb == 2);
	i = 3;
	while ((i * i) <= (unsigned int) nb)
	{
		if (!(nb % i))
			return (0);
		i += 2;
	}
	return (1);
}
